#!/bin/bash

# Створюємо список опцій
options=("Опція 1" "Опція 2" "Опція 3" "Вихід")

# Виводимо меню
echo "Оберіть опцію:"
for i in "${!options[@]}"; do
    echo "$((i+1)). ${options[$i]}"
done

# Зчитуємо ввід користувача
read -rp "#? " choice

# Обробляємо вибір
case $choice in
    1) echo "Ви обрали Опцію 1";;
    2) echo "Ви обрали Опцію 2";;
    3) echo "Ви обрали Опцію 3";;
    4) echo "Ви обрали Вихід"; exit;;
    *) echo "Неправильний вибір";;
esac
